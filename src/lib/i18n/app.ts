function resolveAppLocale(): AppLocale {
  const language = getBrowserLanguage()?.toLowerCase().trim() ?? ''
  const [primary] = language.split('-')
  if (primary === 'es') return 'es'
  return FALLBACK_LOCALE
}

export function getAppLocale(): AppLocale {
  return resolveAppLocale()
}

function interpolate(template: string, vars: Record<string, string | number> | undefined): string {
  if (!vars) return template
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (full, key: string) => {
    const value = vars[key]
    return value === undefined ? full : String(value)
  })
}

export function tApp(key: string, vars?: Record<string, string | number>): string {
  const locale = resolveAppLocale()
  const localeMessages = APP_MESSAGES[locale]
  const fallbackMessages = APP_MESSAGES[FALLBACK_LOCALE]
  const template = localeMessages[key] ?? fallbackMessages[key] ?? key
  return interpolate(template, vars)
}
